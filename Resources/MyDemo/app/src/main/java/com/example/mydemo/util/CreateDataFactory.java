    /**
     * 生成1000-9999的4位数ID（无ulead零）
     *
     * @return 4位整数（1000-9999）
     */
    public static int generate4DigitId() {
        // 方法1：使用Random（单线程推荐）
        Random random = new Random();// ignore_security_alert [ByDesign7.4]WeakPRNG
        // 生成0-8999的随机数，加上1000后范围为1000-9999
        int id1 = random.nextInt(9000) + 1000;

        // 方法2：使用ThreadLocalRandom（多线程更高效，推荐）
        // 直接指定范围：1000（包含）到10000（不包含），即1000-9999
        int id2 = ThreadLocalRandom.current().nextInt(1000, 10000);

        return id2; // 返回其中一种结果即可
    }

    /**
     * 生成随机手机号
     **/
    public static String generateRandomPhones() {
        Random random = new Random();// ignore_security_alert [ByDesign7.4]WeakPRNG
        String prefix = PHONE_PREFIXES[random.nextInt(PHONE_PREFIXES.length)];
        // 2. 生成后8位数字（0-9随机）
        StringBuilder suffix = new StringBuilder();
        for (int j = 0; j < 8; j++) {
            suffix.append(random.nextInt(10)); // 生成0-9的随机数
        }
        // 3. 拼接成11位手机号
        return prefix + suffix.toString();
    }


